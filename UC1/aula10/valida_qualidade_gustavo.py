qualidade = int(input("Digite a Qualidade (1, 2 ou 3): "))
if qualidade == 1:
    print("Qualidade selecionada: Primeira")
elif qualidade == 2:
    print("Qualidade selecionada: Segunda")
elif qualidade == 3:
    print("Qualidade selecionada: Terceira")
else:
    print("Qualidade inexistente!")
print("Fim do programa.")